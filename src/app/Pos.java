    public void costoProducto() {
        double suma = 0;
        for (int i = 0; i < tblProducto.getRowCount(); i++) {
            suma = suma + Double.parseDouble(tblProducto.getValueAt(i, 2).toString());
        }
        txtSubTotal.setText(Double.toString(suma));
        double total1 = Double.parseDouble(txtSubTotal.getText());
        double iva = (total1 * 19) / 100;

        String ivaTotal = String.format("$ %.2f", iva);
        txtIva.setText(ivaTotal);

        String total = String.format("$ %.2f", total1 + iva);
        txtTotal.setText(total);

        String codigoBarra = String.format("Total: %.2f", iva, total1);
        txtTotal.setText(total);
    }

    public void cambio() {
        double suma = 0, iva = 19;
        double cambio = Double.parseDouble(txtRecibido.getText());
        for (int i = 0; i < tblProducto.getRowCount(); i++) {
            suma = suma + Double.parseDouble(tblProducto.getValueAt(i, 2).toString());
        }
        double cIva = (suma * iva) / 100;
        double cCambio = cambio - (suma + cIva);
        String vueltas = String.format("Total: %.2f", cCambio);
        txtCambio.setText(vueltas);
    }
