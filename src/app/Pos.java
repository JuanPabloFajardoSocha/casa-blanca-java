    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblProducto.getModel();
        int eliminarProducto = tblProducto.getSelectedRow();
        if (eliminarProducto >= 0) {
            model.removeRow(eliminarProducto);
        }
        costoProducto();
        if (cbxMetodoPago.getSelectedItem().equals("Efectivo")) {
            cambio();
        }
        String numero = txtRecibido.getText();
        if (numero == "" && cbxMetodoPago.equals("Efectivo")) {
            txtCambio.setText("");
            txtRecibido.setText("");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

